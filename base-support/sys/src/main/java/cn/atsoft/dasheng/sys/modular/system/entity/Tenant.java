      @TableId(value = "tenant_id", type = IdType.ID_WORKER)
    private Long tenantId;

    /**
     * 租户名称
     */
    @TableField("name")
    private String name;

    /**
     * 租户地址
     */
    @TableField("address")
    private String address;

    /**
     * 租户邮箱
     */
    @TableField("email")
    private String email;

